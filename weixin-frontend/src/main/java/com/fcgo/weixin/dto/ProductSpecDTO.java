package com.fcgo.weixin.dto;

import java.math.BigDecimal;

public class ProductSpecDTO {

    private String id;
    
    private String specName;
    
    private Integer stock;
    
    private BigDecimal originalPrice;
    
    private BigDecimal salesPrice;
    
    private Integer fcgSpecId;
    
    private Integer amountIncrease;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSpecName() {
        return specName;
    }

    public void setSpecName(String specName) {
        this.specName = specName;
    }

    public Integer getStock() {
        return stock;
    }

    public void setStock(Integer stock) {
        this.stock = stock;
    }

    public BigDecimal getOriginalPrice() {
        return originalPrice;
    }

    public void setOriginalPrice(BigDecimal originalPrice) {
        this.originalPrice = originalPrice;
    }

    public BigDecimal getSalesPrice() {
        return salesPrice;
    }

    public void setSalesPrice(BigDecimal salesPrice) {
        this.salesPrice = salesPrice;
    }

    public Integer getFcgSpecId() {
        return fcgSpecId;
    }

    public void setFcgSpecId(Integer fcgSpecId) {
        this.fcgSpecId = fcgSpecId;
    }

    public Integer getAmountIncrease() {
        return amountIncrease;
    }

    public void setAmountIncrease(Integer amountIncrease) {
        this.amountIncrease = amountIncrease;
    }
    
    
    

}
