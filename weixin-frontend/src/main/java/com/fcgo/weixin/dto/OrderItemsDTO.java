package com.fcgo.weixin.dto;

import com.fcgo.weixin.persist.po.OrderItemPO;

/**
 * 订单子项 返回结果集
 * 
 * @author Ww
 */
public class OrderItemsDTO {
    // 订单子表对象
    private OrderItemPO orderItemPO;
    // 商品图片表对象
    private String imageUrl;

    // 是否申请售后
    private int isRefund;
    // 售后ID
    private int ordRefundId;

    private int isTuiHuo;

    public int getIsTuiHuo() {
        return isTuiHuo;
    }

    public void setIsTuiHuo(int isTuiHuo) {
        this.isTuiHuo = isTuiHuo;
    }

    public OrderItemPO getOrderItemPO() {
        return orderItemPO;
    }

    public void setOrderItemPO(OrderItemPO orderItemPO) {
        this.orderItemPO = orderItemPO;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

    public int getIsRefund() {
        return isRefund;
    }

    public void setIsRefund(int isRefund) {
        this.isRefund = isRefund;
    }

    public int getOrdRefundId() {
        return ordRefundId;
    }

    public void setOrdRefundId(int ordRefundId) {
        this.ordRefundId = ordRefundId;
    }

}
