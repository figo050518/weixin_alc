package com.fcgo.weixin.application.dto;

import java.math.BigDecimal;
import java.util.List;

/**
 * 保存订单
 * 
 * @author xiahan
 */
public class OrderSaveDTO {

    private List<OrderConfirmProductDTO> orderConfirmProductDTOs;

    private int shopId;

    private int sellerId;

    private BigDecimal actualPayPrice;

    private BigDecimal productTotalPrice;

    private BigDecimal freightTotalPrice;

    private int orderType;

    private String remark;

    public List<OrderConfirmProductDTO> getOrderConfirmProductDTOs() {
        return orderConfirmProductDTOs;
    }

    public void setOrderConfirmProductDTOs(List<OrderConfirmProductDTO> orderConfirmProductDTOs) {
        this.orderConfirmProductDTOs = orderConfirmProductDTOs;
    }

    public int getShopId() {
        return shopId;
    }

    public void setShopId(int shopId) {
        this.shopId = shopId;
    }

    public int getSellerId() {
        return sellerId;
    }

    public void setSellerId(int sellerId) {
        this.sellerId = sellerId;
    }

    public BigDecimal getActualPayPrice() {
        return actualPayPrice;
    }

    public void setActualPayPrice(BigDecimal actualPayPrice) {
        this.actualPayPrice = actualPayPrice;
    }

    public BigDecimal getProductTotalPrice() {
        return productTotalPrice;
    }

    public void setProductTotalPrice(BigDecimal productTotalPrice) {
        this.productTotalPrice = productTotalPrice;
    }

    public BigDecimal getFreightTotalPrice() {
        return freightTotalPrice;
    }

    public void setFreightTotalPrice(BigDecimal freightTotalPrice) {
        this.freightTotalPrice = freightTotalPrice;
    }

    public int getOrderType() {
        return orderType;
    }

    public void setOrderType(int orderType) {
        this.orderType = orderType;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

}
