package com.fcgo.weixin.common.log;

/**
 * LogCategory.java
 * 
 * @author xiahan
 */
public enum LogCategory {

    /** log controller */
    CONTROLLER("controller"),
    /** log service */
    SERVICE("service"),
    /** log domain */
    DOMAIN("domain"),
    /** log persist layer */
    PERSIST("persist"),
    /** log system info */
    SYSTEM("system"), EXCEPTION("exception");

    private String name;

    private LogCategory(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }
}
